# Napisati program koji za uneti broj u opsegu od 1 do 7 ukljucujuci i 7, 
# ispisuje koji je to dan u nedlji.

broj=int(input('Unesi broj od 1-7: '))

if broj==1:
    print('Ponedeljak')
elif broj==2:
    print('Utorak')
elif broj==3:
    print('Sreda')
elif broj==4:
    print('Cetvrtak')
elif broj==5:
    print('Petak')
elif broj==6:
    print('Subota')
elif broj==7:
    print('Nedelja')